import UIKit

extension UIColor {
    @nonobjc class var maize: UIColor {
        return UIColor(red: 1.0, green: 215.0 / 255.0, blue: 68.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var backgroundGray: UIColor {
        return UIColor(white: 231.0 / 255.0, alpha: 1.0)
    }
}
